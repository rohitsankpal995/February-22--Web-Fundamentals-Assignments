package Feb_16_1022;

public class Swap_2_numbers {
	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		System.out.println("before swaping : " + a + " " + b);
		int temp;
		temp = a;
		a = b;
		b = temp;
		System.out.println("After swaping : " + a + " " + b);

	}

}
