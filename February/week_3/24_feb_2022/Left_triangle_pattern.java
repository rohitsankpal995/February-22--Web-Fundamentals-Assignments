package FEb_24_2022;

public class Left_triangle_pattern {

	public static void main(String[] args) {
		
		int i, j, n=5;   
		  
		for(i=0; i<n; i++)   
		{   
		 
		for(j=0; j<=i; j++)   
		{   
		   
		System.out.print("*");   
		}   
		//throws the cursor in a new line after printing each line  
		System.out.println();   
		}   

	}

}
